    private class VerifyDatabaseTask extends SwingWorker<Integer, Integer> {
        private volatile boolean runnable = true;
        private java.util.List<String> codes;
        
        public VerifyDatabaseTask(java.util.List<String> codes) {
            this.codes = codes;            
        }
        
        public void _stop() {
            runnable = false;
        }
        
        public Integer doInBackground() {
            final MainFrame m = (MainFrame)javax.swing.SwingUtilities.getAncestorOfClass(MainFrame.class, WizardVerifyDatabaseJPanel.this);
            final StockHistorySerializer stockHistorySerializer = m.getStockHistorySerializer();
            
            int count = 0;
            int success = 0;
            
            publish(count);
            
            root:
            for(String code : codes) {
                final StockHistoryServer stockHistoryServer = stockHistorySerializer.load(code);
                    
                if(stockHistoryServer != null)
                    success++;
                
                if(isCancelled() || !runnable) {
                        break root;
                }
                                    
                count++;
                publish(count);
            }
            
            return success;
         }

         protected void process(java.util.List<Integer> progresses) {
             for (Integer progress : progresses) {
                 jProgressBar1.setValue(progress);
                 jLabel1.setForeground(Color.BLACK);
                 jLabel1.setText("Verification in progress. " + progress + " out of " + codes.size() + " stock(s) completed");
             }
         }
         
        public void done() {
            Integer success = 0;
            
            try {
                success = get();
            }
            catch(InterruptedException exp) {
                log.error("", exp);
            }
            catch(java.util.concurrent.ExecutionException exp) {
                log.error("", exp);
            }
            
            jLabel1.setForeground(Color.BLUE);
            jLabel1.setText("Verification completed with " + success + " out of " + codes.size() + " stock(s) are valid");
            propertyChangeSupport.firePropertyChange("verify", false, true);
        }
    }
    
    public void startVerify(java.util.List<String> codes) {
        if(verifyHistoryTask != null) {
            verifyHistoryTask._stop();
            try {
                verifyHistoryTask.get();
            }
            catch(InterruptedException exp) {
                log.error("", exp);
            }
            catch(java.util.concurrent.ExecutionException exp) {
                log.error("", exp);
            }
        }
        
        jProgressBar1.setMaximum(codes.size());
        
        verifyHistoryTask = new VerifyDatabaseTask(codes);
        verifyHistoryTask.execute();
    }
    
    public void stopVerify() {
        if(verifyHistoryTask != null) {
            verifyHistoryTask._stop();
            try {
                verifyHistoryTask.get();
            }
            catch(InterruptedException exp) {
                log.error("", exp);
            }
            catch(java.util.concurrent.ExecutionException exp) {
                log.error("", exp);
            }
        } 
        
        verifyHistoryTask = null;
    }


    
